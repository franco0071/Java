// Suma de elementos: Escribir un programa que cree un array de 3 enteros e imprima por consola la suma 
// de todos sus elementos, sin usar el bucle for.


public class Ejercicio1 {
    public static void main(String[] args) {
        int[] array = {2, 4, 6}; // Crear el array de 3 enteros
        
        int suma = array[0] + array[1] + array[2]; // Sumar los elementos directamente
        
        System.out.println("La suma de los elementos del array es: " + suma);
    }
}
